package daii.bookListing;

public class Books {
    private String mTitle;
    private String mAuthor;

    public Books(String title, String author) {
        mTitle = title;
        mAuthor = author;
    }

    public String getmTitle() {
        return mTitle;
    }

    public String getmAuthor() {
        return mAuthor;
    }

}
